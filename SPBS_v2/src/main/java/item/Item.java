package item;

public class Item {
	private int itemID;
	private int itemStock;
	private int itemPrice;
	private int itemSale;
	private int itemAvailable;
	private int itemSaleCount;
	private String itemName;
	private String itemContent;
	private String itemSeason;
	private String itemContentImagePath;
	private String itemContentImageRealPath;
	private String itemBigCategory;
	private String itemSmallCategory;
	private String itemUnit;
	
	public int getItemID() {
		return itemID;
	}
	public void setItemID(int itemID) {
		this.itemID = itemID;
	}
	public int getItemStock() {
		return itemStock;
	}
	public void setItemStock(int itemStock) {
		this.itemStock = itemStock;
	}
	public int getItemPrice() {
		return itemPrice;
	}
	public void setItemPrice(int itemPrice) {
		this.itemPrice = itemPrice;
	}
	public int getItemSale() {
		return itemSale;
	}
	public void setItemSale(int itemSale) {
		this.itemSale = itemSale;
	}
	public int getItemAvailable() {
		return itemAvailable;
	}
	public void setItemAvailable(int itemAvailable) {
		this.itemAvailable = itemAvailable;
	}
	public int getItemSaleCount() {
		return itemSaleCount;
	}
	public void setItemSaleCount(int itemSaleCount) {
		this.itemSaleCount = itemSaleCount;
	}
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getItemContent() {
		return itemContent;
	}
	public void setItemContent(String itemContent) {
		this.itemContent = itemContent;
	}
	public String getItemSeason() {
		return itemSeason;
	}
	public void setItemSeason(String itemSeason) {
		this.itemSeason = itemSeason;
	}
	public String getItemContentImagePath() {
		return itemContentImagePath;
	}
	public void setItemContentImagePath(String itemContentImagePath) {
		this.itemContentImagePath = itemContentImagePath;
	}
	public String getItemContentImageRealPath() {
		return itemContentImageRealPath;
	}
	public void setItemContentImageRealPath(String itemContentImageRealPath) {
		this.itemContentImageRealPath = itemContentImageRealPath;
	}
	public String getItemBigCategory() {
		return itemBigCategory;
	}
	public void setItemBigCategory(String itemBigCategory) {
		this.itemBigCategory = itemBigCategory;
	}
	public String getItemSmallCategory() {
		return itemSmallCategory;
	}
	public void setItemSmallCategory(String itemSmallCategory) {
		this.itemSmallCategory = itemSmallCategory;
	}
	public String getItemUnit() {
		return itemUnit;
	}
	public void setItemUnit(String itemUnit) {
		this.itemUnit = itemUnit;
	}	
}
